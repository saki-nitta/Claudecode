import re, os

BASE = r'D:\Obsidian_Sync\リモート保管庫1\02_Notes\幼児教室プログラム\月別ノート'

def mat_sec(p4, p7):
    if not p4 and not p7:
        return None
    ls = ['## 材料']
    if p4:
        ls.append('**④プログラム**')
        for i in p4: ls.append(f'- [ ] {i}')
        if p7: ls.append('')
    if p7:
        ls.append('**⑦工作**')
        for i in p7: ls.append(f'- [ ] {i}')
    return '\n'.join(ls)

def omocha_sec(items):
    ls = ['## おもちゃ入れ替え']
    for i in items: ls.append(f'- {i}')
    return '\n'.join(ls)

def unc_sec(items):
    if not items: return None
    ls = ['## 要整理']
    for i in items: ls.append(f'- {i}')
    return '\n'.join(ls)

def transform(fname, table, p4, p7, omocha, unc):
    path = os.path.join(BASE, fname)
    with open(path, encoding='utf-8') as f:
        c = f.read()

    # 1. Replace old program section
    new_sec = f'## プログラム（1時間30分）\n\n{table}'
    c = re.sub(r'## プログラム（1時間15分）\n\n.*?(?=\n\n## )', new_sec, c, flags=re.DOTALL)

    # 2. Build insert (材料 + おもちゃ入れ替え)
    parts = []
    m = mat_sec(p4, p7)
    if m: parts.append(m)
    parts.append(omocha_sec(omocha))
    insert = '\n\n'.join(parts)

    # 3. Insert before 準備物
    c = c.replace('\n\n## 準備物', f'\n\n{insert}\n\n## 準備物', 1)

    # 4. Remove 準備物 section
    c = re.sub(r'\n\n## 準備物\n.*?(?=\n\n## )', '', c, flags=re.DOTALL)

    # 5. Add 要整理 at bottom
    u = unc_sec(unc)
    if u:
        c = c.rstrip('\n') + '\n\n' + u + '\n'

    with open(path, 'w', encoding='utf-8') as f:
        f.write(c)
    print(f'OK: {fname}')

